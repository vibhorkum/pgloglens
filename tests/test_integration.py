"""Integration tests for pgloglens using Docker-generated PostgreSQL logs.

These tests validate pgloglens capabilities against real PostgreSQL logs
generated by a Docker container with comprehensive logging enabled.

Run with: pytest tests/test_integration.py -v --docker

Prerequisites:
  1. docker-compose up -d
  2. python tests/docker/generate_logs.py
  3. docker cp pgloglens-test-pg:/var/log/postgresql/postgresql.log tests/docker/
"""

from __future__ import annotations

import json
import os
import subprocess
import sys
from pathlib import Path
from typing import Any, Dict, List

import pytest

# Mark all tests in this module as integration tests
pytestmark = pytest.mark.integration

TESTS_DIR = Path(__file__).parent
DOCKER_LOG = TESTS_DIR / "docker" / "postgresql.log"
SAMPLE_LOG = TESTS_DIR / "sample_pg.log"


def _get_log_path() -> Path:
    """Get the appropriate log file for testing."""
    if DOCKER_LOG.exists():
        return DOCKER_LOG
    # Fall back to sample log for CI without Docker
    return SAMPLE_LOG


def _run_analysis(log_path: Path, **kwargs) -> Dict[str, Any]:
    """Run pgloglens analysis and return parsed JSON output."""
    from pgloglens.parser import LogParser
    from pgloglens.analyzer import Analyzer
    from pgloglens.rca import run_rca

    slow_threshold = kwargs.get("slow_query_threshold_ms", 100.0)

    parser = LogParser(
        slow_query_threshold_ms=slow_threshold,
        filter_database=kwargs.get("database"),
        filter_user=kwargs.get("user"),
    )

    analyzer = Analyzer(
        log_file_paths=[str(log_path)],
        slow_query_threshold_ms=slow_threshold,
        top_queries=kwargs.get("top_queries", 50),
        top_errors=kwargs.get("top_errors", 50),
    )

    entries = parser.parse_file(log_path, show_progress=False)
    result = analyzer.process_entries(entries)
    run_rca(result)

    return result


def _run_cli(args: List[str], log_path: Path | None = None) -> subprocess.CompletedProcess:
    """Run pgloglens CLI command."""
    if log_path is None:
        log_path = _get_log_path()

    cmd = [sys.executable, "-m", "pgloglens.cli"] + args + [str(log_path)]
    return subprocess.run(cmd, capture_output=True, text=True, cwd=TESTS_DIR.parent)


