class TestSlowQueryDetection:
    """Test slow query detection and analysis."""

    def test_detects_slow_queries(self):
        """Should detect queries exceeding threshold."""
        log_path = _get_log_path()
        result = _run_analysis(log_path, slow_query_threshold_ms=100.0)
        assert len(result.slow_queries) > 0, "Should detect slow queries"

    def test_slow_query_statistics(self):
        """Slow queries should have valid statistics."""
        log_path = _get_log_path()
        result = _run_analysis(log_path, slow_query_threshold_ms=100.0)

        for sq in result.slow_queries:
            assert sq.count >= 1, "Count should be at least 1"
            assert sq.avg_duration_ms > 0, "Average duration should be positive"
            assert sq.max_duration_ms >= sq.min_duration_ms, "Max >= Min"
            assert sq.p50_duration_ms <= sq.p95_duration_ms <= sq.p99_duration_ms, "Percentiles ordered"

    def test_query_normalization(self):
        """Queries should be normalized (literals replaced)."""
        log_path = _get_log_path()
        result = _run_analysis(log_path, slow_query_threshold_ms=100.0)

        if result.slow_queries:
            for sq in result.slow_queries:
                # Normalized queries shouldn't have string literals
                assert "'" not in sq.normalized_query or "$" in sq.normalized_query, \
                    "Query should be normalized"

    def test_slow_queries_sorted_by_total_time(self):
        """Slow queries should be sorted by total duration."""
        log_path = _get_log_path()
        result = _run_analysis(log_path, slow_query_threshold_ms=100.0)

        if len(result.slow_queries) >= 2:
            for i in range(len(result.slow_queries) - 1):
                assert result.slow_queries[i].total_duration_ms >= result.slow_queries[i + 1].total_duration_ms


