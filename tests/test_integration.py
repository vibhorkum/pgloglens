class TestEdgeCases:
    """Test edge cases and error handling."""

    def test_empty_log_handling(self, tmp_path):
        """Should handle empty log files."""
        from pgloglens.parser import LogParser

        empty_log = tmp_path / "empty.log"
        empty_log.write_text("")

        parser = LogParser()
        entries = list(parser.parse_file(empty_log, show_progress=False))
        assert entries == []

    def test_malformed_line_handling(self, tmp_path):
        """Should handle malformed log lines gracefully."""
        from pgloglens.parser import LogParser

        bad_log = tmp_path / "bad.log"
        bad_log.write_text("this is not a valid log line\n" * 10)

        parser = LogParser()
        # Should not raise an exception
        entries = list(parser.parse_file(bad_log, show_progress=False))
        # May or may not parse entries depending on format

    def test_mixed_format_handling(self, tmp_path):
        """Should handle logs with some valid and invalid lines."""
        from pgloglens.parser import LogParser

        mixed_log = tmp_path / "mixed.log"
        content = """garbage line
2024-01-15 08:00:01.234 UTC [12345] LOG:  valid message
more garbage
2024-01-15 08:00:02.234 UTC [12346] LOG:  another valid message
"""
        mixed_log.write_text(content)

        parser = LogParser()
        entries = list(parser.parse_file(mixed_log, show_progress=False))
        assert len(entries) >= 2, "Should parse valid lines"


